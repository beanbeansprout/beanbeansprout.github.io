const maxPalettes = 12;
var paletteValues, palettes, combinedPalettes;
var pxWidth = document.getElementById("pxWidth"), pxHeight = document.getElementById("pxHeight");
var imgWidth = document.getElementById("imgWidth"), imgHeight = document.getElementById("imgHeight");

var canvas = document.getElementById("canvas");
var ctx = canvas.getContext("2d");

function draw() {  
  paletteValues = [];
  palettes = [];
  combinedPalettes = [];
  
  ctx.canvas.width = imgWidth.value;
  ctx.canvas.height = imgHeight.value;
  
  for (let i = 0; i < maxPalettes; i++) {
    paletteValues[i] = document.getElementById("palette" + (i + 1));
  }
  
  for (let i = 0; i < maxPalettes; i++) {
    if (paletteValues[i].value == "Red") {palettes[i] = redColors}
    else if (paletteValues[i].value == "Orange") {palettes[i] = orangeColors}
    else if (paletteValues[i].value == "Yellow") {palettes[i] = yellowColors}
    else if (paletteValues[i].value == "Green") {palettes[i] = greenColors}
    else if (paletteValues[i].value == "Blue") {palettes[i] = blueColors}
    else if (paletteValues[i].value == "Purple") {palettes[i] = purpleColors}
    else if (paletteValues[i].value == "Cyan") {palettes[i] = cyanColors}
    else if (paletteValues[i].value == "Pink") {palettes[i] = pinkColors}
    else if (paletteValues[i].value == "Gray") {palettes[i] = grayColors}
    else if (paletteValues[i].value == "Brown") {palettes[i] = brownColors}
    else if (paletteValues[i].value == "White") {palettes[i] = whiteColors}
    else if (paletteValues[i].value == "All") {palettes[i] = allColors}
    else if (paletteValues[i].value == "Default") {palettes[i] = defaultColors}
    else {continue}
    
    combinedPalettes = combinedPalettes.concat(palettes[i]);
  }
  
  for (let i = 0; i < canvas.width; i += parseInt(pxWidth.value)) {
    for (let j = 0; j < canvas.height; j += parseInt(pxHeight.value)) {
       ctx.fillStyle = combinedPalettes[Math.floor(Math.random() * combinedPalettes.length)];
       ctx.fillRect(i, j, parseInt(pxWidth.value), parseInt(pxHeight.value));
    }
  }
  
  if (checkIfSolved()) {
    document.getElementById("key").innerHTML = "The puzzle is solved!"; 
  }
  
  for (let i = 0; i < maxPalettes; i++) {
    paletteValues[i].value = "";
  }
  
  pxWidth.value = "";
  pxHeight.value = "";
  
  imgWidth.value = "";
  imgHeight.value = "";
}

function checkIfSolved() {
  if (
    palettes[0] == pinkColors &&
    palettes[1] == grayColors &&
    palettes[2] == greenColors &&
    palettes[3] == purpleColors &&
    palettes[4] == redColors &&
    palettes[5] == defaultColors &&
    palettes[6] == allColors &&
    palettes[7] == cyanColors &&
    palettes[8] == orangeColors &&
    palettes[9] == whiteColors &&
    palettes[10] == yellowColors &&
    palettes[11] == grayColors &&
    pxWidth.value == "37" &&
    pxHeight.value == "59"
  ) {
    return true;
  } else {
    return false; 
  }
}